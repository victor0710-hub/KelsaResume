let currentIndex = 0;
let currentLang = localStorage.getItem("portfolioLanguage") || "en";
let isAnimating = false;

const content = document.getElementById("content");
const heroImage = document.getElementById("heroImage");
const tabs = document.querySelectorAll(".tab");

const btnEN = document.getElementById("btnEN");
const btnTH = document.getElementById("btnTH");

const sidebarName = document.getElementById("sidebarName");
const sidebarTitle = document.getElementById("sidebarTitle");
const sidebarPhone = document.getElementById("sidebarPhone");
const sidebarEmail = document.getElementById("sidebarEmail");
const sidebarLocation = document.getElementById("sidebarLocation");
const downloadCvBtn = document.getElementById("downloadCvBtn");

function renderSidebar() {
  const sidebar = pageData[currentLang].sidebar;

  sidebarName.textContent = sidebar.name;
  sidebarTitle.textContent = sidebar.title;
  sidebarPhone.textContent = sidebar.phone;
  sidebarEmail.textContent = sidebar.email;
  sidebarLocation.textContent = sidebar.location;
  downloadCvBtn.textContent = sidebar.download;

  btnEN.classList.toggle("active", currentLang === "en");
  btnTH.classList.toggle("active", currentLang === "th");
}

function renderSection(index) {
  const section = pageData[currentLang].sections[index];

  heroImage.src = section.image;
  heroImage.alt = section.tabTitle + " photo";

  content.innerHTML = `
    <p class="kicker">${section.kicker}</p>
    <h2>${section.title}</h2>
    <div class="section-body">${section.html}</div>
  `;

  tabs.forEach((tab, tabIndex) => {
    tab.classList.remove("active");
    tab.title = pageData[currentLang].sections[tabIndex].tabTitle;
    tab.setAttribute("aria-label", pageData[currentLang].sections[tabIndex].tabTitle);
  });

  tabs[index].classList.add("active");

  lucide.createIcons();
}

function changeSection(index) {
  if (isAnimating || index === currentIndex) return;

  isAnimating = true;
  content.classList.add("fade-out");
  heroImage.style.opacity = "0";
  heroImage.style.transform = "translateY(10px) scale(0.98)";

  setTimeout(() => {
    currentIndex = index;
    renderSection(currentIndex);
    content.classList.remove("fade-out");
    heroImage.style.opacity = "1";
    heroImage.style.transform = "translateY(0) scale(1)";

    setTimeout(() => {
      isAnimating = false;
    }, 350);
  }, 350);
}

function changeLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("portfolioLanguage", currentLang);

  renderSidebar();
  renderSection(currentIndex);
}

tabs.forEach(tab => {
  tab.addEventListener("click", () => {
    const index = Number(tab.dataset.index);
    changeSection(index);
  });
});

btnEN.addEventListener("click", () => changeLanguage("en"));
btnTH.addEventListener("click", () => changeLanguage("th"));

window.addEventListener("wheel", event => {
  if (isAnimating || window.innerWidth <= 900) return;

  if (event.deltaY > 0) {
    const next = (currentIndex + 1) % pageData[currentLang].sections.length;
    changeSection(next);
  } else {
    const previous = (currentIndex - 1 + pageData[currentLang].sections.length) % pageData[currentLang].sections.length;
    changeSection(previous);
  }
});


const backgroundMusic = document.getElementById("backgroundMusic");
const musicToggle = document.getElementById("musicToggle");

const isMobileDevice = window.matchMedia("(max-width: 900px)").matches;

let musicEnabled = false;

if (backgroundMusic && musicToggle) {
  backgroundMusic.volume = 0.16;

  if (isMobileDevice) {
    backgroundMusic.pause();
    backgroundMusic.muted = true;
    musicToggle.style.display = "none";
  }

  musicToggle.addEventListener("click", async () => {
    if (isMobileDevice) return;

    if (!musicEnabled) {
      try {
        backgroundMusic.muted = false;
        await backgroundMusic.play();
        musicEnabled = true;
        musicToggle.textContent = "Music On";
      } catch (error) {
        console.log("Music could not start:", error);
      }
    } else {
      backgroundMusic.pause();
      musicEnabled = false;
      musicToggle.textContent = "Music Off";
    }
  });
}


renderSidebar();
renderSection(currentIndex);
lucide.createIcons();
